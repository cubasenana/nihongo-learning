def has_verb(pos):
    return bool(re.search(r'動[123]', pos)) or pos == '補動'

def verb_group(pos):
    if re.search(r'動1', pos): return '一类'
    if re.search(r'動2', pos): return '二类'
    if re.search(r'動3', pos): return '三类'
    if pos == '補動': return '补助'
    return None

