def classify(word, pos, reading, meaning):
    # Step 0: 词条硬编码覆盖
    if word in WORD_OVERRIDES:
        return WORD_OVERRIDES[word]

    # Step 1: 动词
    vg = verb_group(pos)
    if vg:
        return f'动词·{vg}'

    # Step 2: い形容词
    if 'イ形' in pos and '接尾' not in pos:
        return '形容词·い形'

    # Step 3: な形容词（含名・ナ形）
    if 'ナ形' in pos:
        return '形容词·な形'

    # Step 4: 代词
    if any(k in pos for k in ('代',)):
        if pos in ('代', '代・名', '名・代', '代・接頭', '代・副・感', '名・代・副'):
            return '基础·代词指示'

    # Step 5: 感叹词
    if pos in ('感', '感・終助'):
        return '基础·感叹寒暄'

    # Step 6: 接续词/助词
    if pos in ('接', '接・接助', '接・接助・終助', '接・副助', '接助', '格助', '副助'):
        return '基础·接续助词'

    # Step 7: 连体词
    if '連体' in pos and '名' not in pos and 'ナ形' not in pos:
        return '基础·连体词'

    # Step 8: 接头/接尾
    if pos in ('接頭', '接頭・接尾', '接尾・名'):
        return '基础·接头接尾'
    if pos == '接尾':
        q_kw = ['层','楼','个','册','台','辆','瓶','杯','次','月','岁','人','点',
                 '号','名','回','度','份','本','枚','匹','头','位','双','套','件']
        if any(k in meaning for k in q_kw):
            return '基础·量词单位'
        return '基础·接尾'

    # Step 9: 惯用表达
    if pos in ('連語', '成句'):
        return '惯用表达'

    # Step 10: 造语
    if pos == '造':
        return '基础·造语'

    # Step 11: 副词（不含名词的纯副词）
    if '副' in pos and '名' not in pos:
        return '副词'

    # Step 12: 名词 + 按释义分桶
    if '名' in pos or '副' in pos:
        t = theme_by_meaning(word, pos, reading, meaning)
        if t:
            return t
        # 副词兜底
        if '副' in pos:
            return '副词'
        return '名词·其他'

    # Step 13: 造语成分（含名）
    if '造' in pos:
        return '基础·造语'

    return '其他'


def main():
    rows = []
    with open(INPUT, 'r', encoding='utf-8') as f:
        reader = csv.reader(f, delimiter='\t')
        next(reader)
        for row in reader:
            if len(row) < 5:
                continue
            word = row[0]
            pos = row[2] if len(row) > 2 else ''
            reading = row[3] if len(row) > 3 else ''
            meaning = row[4] if len(row) > 4 else ''
            theme = classify(word, pos, reading, meaning)
            rows.append((theme, word, pos, reading, meaning))

    rows.sort(key=lambda x: x[0])
    counter = Counter(r[0] for r in rows)

    print("=" * 60)
    print(f"总词数: {len(rows)}")
    print("=" * 60)
    print(f"{'主题桶':<24} {'词数':>5}")
    print("-" * 34)
    for theme, count in sorted(counter.items(), key=lambda x: (-x[1], x[0])):
        print(f"{theme:<24} {count:>5}")

    # 只打印"名词·其他"的全部内容供审查
    print("\n" + "=" * 60)
    print("【名词·其他】完整列表（待二次分拣）:")
    print("=" * 60)
    for theme, word, pos, reading, meaning in rows:
        if theme == '名词·其他':
            print(f"  {word}({reading}) [{pos}] {meaning}")


if __name__ == '__main__':
    main()
