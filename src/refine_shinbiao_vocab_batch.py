def dedent_block(text: str) -> str:
    return textwrap.dedent(text).strip() + "\n"


def replace_regex(path: Path, pattern: str, replacement: str) -> None:
    text = path.read_text(encoding="utf-8")
    new_text, count = re.subn(pattern, replacement, text, flags=re.MULTILINE | re.DOTALL)
    if count != 1:
        raise RuntimeError(f"Expected exactly one replacement in {path}, got {count}")
    path.write_text(new_text, encoding="utf-8")


